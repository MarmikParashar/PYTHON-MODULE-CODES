# If Statement
a = 33
b = 200
if b > a:
    print ("b is greater than a")
    
# elif Statement or if else Statement
a = 33
b = 33
if b > a:
    print ("b is greater than a")
elif a == b:
    print("a and b are equal")
    
# Nested if else Statement
x = 51
if x > 20:
    print ("Above twenty,")
elif x > 30:
    print ("and also above 30!")
else:
    print ("but not above 30.")


